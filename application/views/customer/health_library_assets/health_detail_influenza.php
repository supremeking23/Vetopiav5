</p><p><img width="272" height="300" style="margin: 5px; width: 268px; height: 296px; float: right;" src="<?php echo site_url();?>assets/images/library/4128528.jpg" documentid="0" tabletype="20" fileid="4128528" subdocfilerepositoryid="5711432"></p>
<p>Just like people, dogs can be affected by different strains of influenza, a highly contagious respiratory infection. There are two strains of canine influenza known to affect dogs internationally. H3N8 broke out around 2004 in Florida and continues to cause sporadic disease; H3N2, a milder strain first seen in Chicago in 2015, is closely related to an Asian strain first identified in 2006. These viruses are different strains of Influenza Type A. Whether or not H3N2 will have the staying power of H3N8 remains to be seen.</p>
<p>After exposure, some dogs will produce enough antibodies that they don’t have any signs of illness. The signs in both strains range from fevers, listlessness, coughing, sneezing, and a runny nose to life-threatening pneumonia, but typically it’s much like having <a href="http://www.veterinarypartner.com/Content.plx?P=A&amp;S=0&amp;C=0&amp;A=600&amp;EVetID=900000083" target="_blank">kennel cough</a>.</p>
<p><strong>Where have cases been reported?</strong></p>
<p>In May 2017, canine H3N2 influenza was diagnosed in dogs in Florida, Georgia, North Carolina, South Carolina, Texas, Kentucky, Tennessee, Missouri, Louisiana, and Illinois. This was the same, newer strain of H3N2 involved in the 2015 outbreak in Chicago. More cases are possible as the this outbreak may or may not spread.&nbsp;</p>
<p>For the newer H3N2 strain, geographic locations expanded in the months after the initial outbreak in March, 2015.</p>
<p><strong>What are the signs?</strong></p>
<p>There is a mild form of the disease and a more severe form that is accompanied by pneumonia.</p>
<p><em>Mild form</em> <br>Dogs suffering with the mild form of influenza develop a soft, moist cough that persists for 10 to 30 days. They may also be lethargic and have reduced appetite and a fever. Sneezing and discharge from the eyes and/or nose may also be observed. Some dogs have a dry cough similar to the traditional kennel cough caused by <br>Bordetella bronchiseptica/parainfluenza virus complex. Dogs may also have a thick nasal discharge, which is usually caused by a secondary bacterial infection.</p>
<p><em>Severe form</em> <br>Dogs with the severe form of influenza develop high fevers (104&#8304;F - 106&#8304;F, 40&#8304;C&nbsp;- 41&#8304;C; normal is 101&#8304;F -&nbsp;102&#8304;F, 38&#8304; - 39&#8304;C) and have clinical signs of pneumonia, such as increased respiratory rates and effort.&nbsp;<a href="http://www.veterinarypartner.com/Content.plx?P=A&amp;S=0&amp;C=0&amp;A=1758&amp;EVetID=900000083" target="_blank">Pneumonia</a> may be due to a secondary bacterial infection.</p>
<p>Fatal cases of pneumonia resulting from infection with influenza have been reported in dogs, but the fatality rate is less than 10 percent. Most dogs recover in two to three weeks.</p>
<p><strong>How is canine influenza transmitted?</strong></p>
<p>A dog is most likely to be contagious before showing any signs at all.</p>
<p>Influenza viruses are hardy: they do not persist in the environment for very long. However, they do spread easily between individuals. Transmission generally requires direct contact with an infected and contagious dog's fresh saliva or oro-nasal secretions. The problem is that an infected dog is usually contagious before showing any clinical signs. Therefore, apparently healthy dogs can transmit the disease.</p>
<p><strong>When should I see the veterinarian?</strong></p>
<p>A dog living in an area where outbreaks are being reported should be considered to have canine flu until proven otherwise. If your dog is from such an area, or there has been mention of canine flu on your local news, and he has a cough (and especially if he's feeling sick), see your veterinarian. Don't ignore it. Canine flu is very contagious, so clinics might request that you come in through a separate entrance.</p>
<p>The tests and treatments your veterinarian might recommend depend on the severity of the illness. For mild disease, the veterinarian might take samples to identify the cause, but only treat the signs. For more severe cases, chest X-rays (radiographs) are often taken to look for pneumonia. Dogs with severe disease can require hospitalization with oxygen and fluid therapy.</p>
<p>The very young and seniors (who may have compromised respiratory systems or concurrent diseases associated with age) may be more likely to have severe signs of illness.</p>
<p><strong>Should I vaccinate?</strong></p>
<p>As with human flu shots, a vaccine for one strain doesn’t help prevent another strain. The existing vaccine for H3N8 does not prevent the new H3N2. The new vaccine for H3N2 that became available in late 2015 does not protect against H3N8. As with most infectious respiratory disease viruses, the vaccine does not protect completely against or eliminate the virus, but reduces how ill your dog can be with it as well as lessening your dog’s ability to transmit the virus to other dogs.</p>
<p>Initially, the vaccine for H3N8 is given two to four weeks apart, and the second one should be completed at least three to four weeks before the dog goes to anywhere like a boarding kennel or dog show. The H3N2 vaccine must be given twice spaced at three weeks apart. Flu viruses require relatively close contact to spread from individual to individual and do not persist well in the environment.</p>
<p>A regular kennel cough vaccination will not prevent influenza.</p>
<p>To decide whether or not your dog should be vaccinated for either strain, talk to your veterinarian about the likelihood of any dog getting either strain in your area.</p>
<p><strong>How is a diagnosis made?</strong></p>
<p>In a perfect world there would be a simple test that could be performed on a single sample and yield unequivocal results, but that's not the case with canine flu. Tests that broadly detect influenza A virus should effectively detect both H3N8 and H3N2 strains. However, tests targeted directly at the older H3N8 are unlikely to identify H3N2 infection because of limited cross-reaction between their respective antibodies. Commercial PCR assays against H3N2 are currently unclear and it has been reported that tests used in several laboratories will not detect this virus. Testing for viral shedding (e.g., viral antigen ELISA, PCR, virus isolation) is effective but needs to be done as early in the course of disease as possible because shedding is generally early and brief. Checking blood for antibodies tends to be more effective later on, but that's only if the test can detect the strain involved. More information about specific testing for H3N2is expected in the near future as we continue to learn about this virus.</p>
<p><strong>What is treatment?</strong></p>
<p>Dogs with mild signs receive supportive care, typically fluids, cough suppressants, or anti-viral medication, depending on their signs and how long the dog has been sick. Severely affected patients usually get antibiotics to prevent or treat pneumonia. Pneumonia cases often require hospitalization.</p>
<p>In high-risk cases antibiotics are given to control secondary infections. Pneumonia results from secondary bacterial infections (i.e. bacteria invading the lung after the virus has damaged the tissue and compromised its ability to defend itself). Pneumonia in dogs is almost always secondary after some other condition has damaged the lung, and treatment is similar regardless of the cause. Unlike other pneumonias or respiratory diseases, the anti-viral medication&nbsp;<a href="http://www.veterinarypartner.com/Content.plx?P=A&amp;S=0&amp;C=0&amp;A=1885&amp;EVetID=900000083" target="_blank">oseltamivir (Tamiflu®)</a> can be helpful but only if used early in the course of infection or to prevent infection in exposed dogs.</p>
<p>A rapid onset of disease - four to six hours - is matched by an equally rapid improvement in clinical signs after treatment begins.</p>
<p><strong>Can&nbsp;the influenza&nbsp;be prevented?</strong></p>
<p>The best preventative measures are to limit or prevent exposure as lifestyle plays a factor in the risk of getting either strain of flu. Dogs that go to day care, dog parks, performance competitions, dog shows, training classes, or boarding kennels have a higher risk. Dogs that spend most of their time at home or rarely come into contact with other dogs have a lower risk.</p>
<p>Don’t let your dog socialize with coughing dogs.</p>
<p><strong>Can people or cats be infected by dogs?</strong></p>
<p>There is no evidence that people can get H3N8. While there is also no evidence that people can contract H3N2, studies in Asia have shown limited transmission to cats. Sheltered cats in Indiana were found to have H3N2;&nbsp;however, the canine vaccine will not work for cats. In Asia, the H3N2 strain that infected cats and caused disease was considered to be of avian origin. Current information about the U.S. H3N2 strain suggests that it might be of porcine origin.</p>
