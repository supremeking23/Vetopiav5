</p><p><strong>What is Xylitol?</strong></p>
<p><img width="215" height="287" style="margin: 5px; width: 215px; height: 287px; float: right;" src="<?php echo site_url()?>assets/images/library/4127510.jpg" imgid="4127510" fileid="4127510" tabletype="20" last="true" subdocfilerepositoryid="13294512" documentid="7961147"></p>
<p>Xylitol is a white, crystalline sugar alcohol that is used as a sugar substitute sweetener in many products. In the United States, the use of xylitol has grown rapidly over the last few years. It is increasingly found in sugar-free gum, candy, and foods. It is also available in granulated form for baking. It is popular among diabetics and those on low-carbohydrate diets. It also is increasingly being included in toothpastes and other oral hygiene products due to its anti-cavity properties. Xylitol may be listed on product labels using other synonyms, such as Eutrit, Kannit, Newtol, Xylite, Torch, or Xyliton.</p>
<p><strong>How is Xylitol Different in Animals versus Humans?</strong></p>
<p>In humans, xylitol is absorbed slowly and has little to no effect on blood sugar or insulin levels. However, in dogs, xylitol is quickly absorbed into the bloodstream within 30 minutes. It then acts as a strong, dose-dependent promoter of insulin release. This causes profound hypoglycemia (low blood sugar). If the hypoglycemia is severe enough, death will ensue.</p>
<p>Moreover, in dogs, xylitol can also cause severe liver damage, leading to bleeding, liver failure, and death.</p>
<p>Similar effects on insulin release are seen in cows, goats, and rabbits. Xylitol's effect on insulin and blood glucose in cats is not clear at this time.</p>
<p><strong>How much Xylitol is Toxic to my Pet?</strong></p>
<p>It takes very little xylitol to cause signs of toxicity in dogs. The ASPCA Animal Poison Control Center (APCC) has reported that dogs who ingest between 50 and 100 mg/kg should receive decontamination and monitoring. Dogs ingesting greater than 100 mg/kg of xylitol should be considered at risk for hypoglycemia and should be treated aggressively. At doses exceeding 500 mg/kg, there is risk of liver failure and very serious effects.</p>
<p>Unfortunately, it is often difficult to determine exactly how many grams of xylitol have been ingested. Although the xylitol content is more commonly listed on food products, this is not the case with chewing gums. Many gum manufacturers consider the level of xylitol in their products to be proprietary information and refuse to disclose it on the label. The amount may differ not only from manufacturer to manufacturer, but from flavor to flavor.</p>
<p>Based on information provided by some manufacturers, the APCC has deduced that the quantity of xylitol in one piece of gum ranges from 0.9 mg to 1,000 mg. That is a vast range. Products that list xylitol as the first ingredient tend to be the most toxic to dogs. For granulated (baking) xylitol, one cup weighs about 190 grams.</p>
<p>In general, we estimate that one piece of gum can potentially cause hypoglycemia in a 20 lb dog.</p>
<p><strong>What are the Symptoms? What Tests can be done to Diagnose Xylitol Toxicity?</strong></p>
<p>Diagnosis is made on history of ingestion, symptoms, and blood work. Because of the rapid progression of the toxic effect, testing for xylitol in the blood is not realistic.</p>
<ul>
<li>Vomiting is often the first symptom</li>
<li>Signs of hypoglycemia (lethargy, weakness) occur rapidly</li>
<li>Diarrhea, collapse and seizures may be seen.</li>
</ul>
<p>Dogs ingesting xylitol-containing gums may not develop signs of hypoglycemia unit 12 hours later.</p>
<p>Dogs developing acute liver failure may not show signs of hypoglycemia after ingesting xylitol. Not all dogs who ingest xylitol will develop liver failure.</p>
<p><strong>What other Toxins Should be Ruled Out?</strong></p>
<p>Other causes of low blood sugar should be ruled out (overdose of insulin, young or toy breed-related hypoglycemia, starvation). In addition, many other toxins can cause liver disease (sago palms, hepatotoxic mushrooms, Tylenolâ„¢, aflatoxins, other drugs). Infectious liver diseases, shunts, and cancer must be ruled out as well.</p>
<p><strong>Is Xylitol Poisoning Treatable?</strong></p>
<ul>
<li>All xylitol exposures should be evaluated by a veterinarian immediately. Because xylitol is absorbed extremely rapidly, early treatment will carry the best prognosis. Xylitol ingestion can be fatal.</li>
<li>There is no antidote for xylitol toxicity.</li>
<li>Remember that even tiny doses, 1 to 2 pieces of xylitol-containing gum, can be toxic to a dog.</li>
<li>Do not induce vomiting or give anything orally unless directed by your veterinarian.</li>
</ul>
<p><strong>What is the Prognosis?</strong></p>
<p>The prognosis is good for uncomplicated hypoglycemia when treatment can be instituted promptly. Liver failure, bleeding disorders, and neurologic symptoms (seizures) generally carry a poor prognosis. Dogs that develop stupor or coma have a grave prognosis.</p>
<p><strong>Is it an Epidemic?</strong></p>
<p>APCC began in 2007 to log xylitol cases. That year, the center received 1,764 calls about xylitol. In 2012, the figure was 3,184. The number of products containing xylitol has been steadily rising over the last few years, with a resultant surge in xylitol cases reported to the ASPCA Animal Poison Control Center.</p>
<p>Xylitol poisoning is preventable. Pet-owning households should consider not consuming Xylitol-containing foods or gums.</p>
