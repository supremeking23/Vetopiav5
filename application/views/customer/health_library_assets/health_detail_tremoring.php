          <p>

</p><p>Dogs are often brought in for tremoring or shivering episodes. The patient can be any age or any size. Tremors can involve only certain muscles or body areas or the entire dog. Finding the reason behind tremors is tricky as tremoring occurs for many reasons.</p>
<p>The first step is to consider normal reasons for shivering. The patient is:</p>
<ul>
<li>cold</li>
<li>frightened</li>
<li>asleep and dreaming.</li>
</ul>
<p>Usually these can be excluded from being a disease by providing a blanket or sweater, comforting the anxious patient, or rousing the sleeping patient.</p>
<p>Elderly dogs with muscle atrophy tend to suffer muscle fatigue and muscles will shake when over-worked. This type of tremoring is often pretty easy to distinguish as the patient is elderly and the tremors begin when the patient has been standing longer than he would prefer. When muscle becomes fatigued, tremoring helps release stored glycogen/glucose to permit continued muscle contraction. Tremoring generally stops when the muscle is allowed to relax and the patient rests.<br><br>Mild shivers are rarely a significant symptom; it is the more violent involuntary tremors and twitches that indicate neuromuscular disease.</p>
<p><strong>Only Certain Body Parts Tremor</strong></p>
<p>If only certain body parts tremor, there are some neuromuscular diseases to consider.</p>
<p><em>Intention tremors<br></em>Diseases involving the cerebellum of the brain will lead to tremors when the patient directs his attention to a task. The patient may sit quietly without tremors but if a bowl of food is put out, tremors of the neck and head erupt when the patient reaches for the food.</p>
<p>This sort of tremoring generally goes way beyond what might be described as shivering or even twitching. A classic example of cerebellar intention tremors is seen in kittens born with cerebellar hypoplasia, a neurologic condition stemming from the mother becoming infected with or vaccinated for <a href="http://www.veterinarypartner.com/Content.plx?P=A&amp;S=0&amp;C=0&amp;A=1983&amp;EVetID=900000083" target="_blank">feline distemper</a> during pregnancy.</p>
<p>Cerebellar disease certainly does not have to be congenital. Any damage to the cerebellum (trauma, tumor, infection etc.) can produce an intention tremor. An intention tremor is highly suggestive of disease in the cerebellum, though, and diagnostics should be pursued to uncover the nature of that disease. See a video of these <a href="https://www.youtube.com/watch?v=A8HZWS4hBk4" target="_blank">wobbly kittens.</a></p>
<p><em>Idiopathic Head Tremors</em><br>No one really knows why this occurs but some dogs have episodes of head bobbing (usually up and down but can be side to side). Breeds that seem over-represented are Boxers, English bulldogs, beagles, and Doberman pinschers. This condition is totally unresponsive to seizure medications and the best way to curtail an episode seems to be to focus the dog’s attention on a toy or treat. Episodes tend to get milder with age. See this <a href="https://www.youtube.com/watch?v=ArJUUlmaNXU" target="_blank">boxer's head tremors</a>.<br>&nbsp;</p>
<p style="text-align: left;"><em>Distemper myoclonus</em><br>Dogs that survive a canine distemper infection may suffer from seizures, paralysis or muscle twitching long term. Often a young dog is adopted from the shelter or rescue and her history is unknown. Distemper myoclonus appears as involuntary twitches of muscle groups anywhere on the body. Multiple areas can be involved. These twitches are not seizures and do not respond to seizure medications. Numerous medications have been used with inconsistent results. The most successful treatment has involved injections of cosmetic Botox® into the muscle group involved, but this therapy is expensive and many dogs do not seem particularly inconvenienced by their myoclonus. In this <a href="https://www.youtube.com/watch?v=G6LSt0fO36E" target="_blank">video</a>, the black dog is showing myoclonus of his jaw and neck muscles. <em><br></em><br><strong>Whole Body Tremors</strong></p>
<p>If the entire dog is shivering and shaking, then we must consider diseases that involve the entire dog. Normal muscles rely on oxygen, nutrients, and electrolytes from the bloodstream as well as normal electrical stimulation from the nervous system. We need to look at problems with these systems:</p>
<ul>
<li>Hypocalcemia (low blood calcium, especially if nursing puppies)</li>
<li>Hypoglycemia (low blood sugar)</li>
<li>Toxins (especially snail bait and insecticides)</li>
<li>“Shaky White Dog syndrome” (which does not actually require that the patient be a white dog), also called non-suppurative meningoencephalomyelitis.</li>
<li>Seizures/epilepsy.</li>
</ul>
<p>Severe tremoring can elevate the body temperature dangerously high and cause brain damage. Patients for which tremoring is an emergency are generally clearly mentally distressed by the involuntary motions or disoriented from the underlying neurologic disease.</p>
<p>Many toxins can cause twitches and involuntary muscle tremors, so it is important to let your veterinarian know what products are in your home. What flea products do you use? Do you have pesticides in the garden? These products are frequently neurotoxins that produce seizures and twitching. Snail bats containing metaldehyde are particularly common, as are permethrin-based flea products (generally safe for dogs but cats are uniquely sensitive). Tremors are a notable feature of chocolate toxicity. Obviously, poisoning is an emergency so you will need to see your veterinarian or local emergency service right away.</p>
<p><em>The Animal Poison Control Center is a 24-hour service run by the ASPCA. Consultations with veterinary toxicologists run around $65 but are available around the clock in the event of an emergency. It is a good idea to keep their phone number handy.888-426-4435</em></p>
<p><em>Note: Free Animal Poison Control Center consultation is included if you have a full-service registration of a HomeAgain microchip ID. In this case, the phone number is:&nbsp; 888-HOMEAGAIN then select the medical emergency option.</em></p>
<p>Brain diseases can produce seizures that can manifest as persistent but fine tremoring or more violent convulsions. Metabolic diseases, such as low blood sugar or low blood calcium, can lead to tremors and even seizures. Muscle diseases such as tetanus can lead to involuntary muscle contraction. Twitches and tremors may be intermittent and are certainly not always emergencies; however, blood testing and possibly advanced neurology testing may be needed. Your first step is going to be an evaluation with your veterinarian to narrow down the search and complete the necessary basic tests. Not all veterinarians are comfortable diagnosing neurologic disease, so discuss with your veterinarian whether referral to a specialist would be best for you and your pet. The best treatment plan can be made from there.</p>
<p>The small white dog in this video has <a href="https://www.youtube.com/watch?v=rc9nb6alocQ" target="_blank">White Shaker syndrome</a>. It is generally controlled with immune-suppressive doses of corticosteroids. Prognosis is generally good.</p>
