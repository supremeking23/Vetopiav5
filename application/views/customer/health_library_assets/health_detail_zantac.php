<p>Available in 75 mg tablets, 150 mg tablets, 300 mg tablets oral syrup and injectable&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <br><br><strong>Background</strong></p>
<p>Stomach ulceration in humans is a prominent medical condition and there has long been pressure to develop effective and convenient ways to address it. Until relatively recently, we relied on simply neutralizing stomach acid by pouring alkaline solutions (i.e., Alka Seltzer, Tums, Rolaids, etc.) into the stomach. This was a temporary and often incomplete solution to an on-going stomach acid problem. In fact, stomach ulceration is a complicated process and more sophisticated treatments have become available.<br><br>Controlling stomach acid remains an important part of treating and preventing stomach ulcers. Acid secretion by the parietal cells of the stomach is stimulated by three main things: gastrin (a hormone secreted when there is food in the stomach to digest), acetylcholine (a neurotransmitter) and histamine (the same histamine that is involved in allergy).<br><br>There are two types of histamine receptors in the body: H1 receptors that are involved in the symptoms of hay fever, and H2 receptors that are located in the stomach and turn on stomach acid secretion. Ranitidine is a special antihistamine, as are its' cousins <a href="https://veterinarypartner.vin.com/doc/?id=7391380&amp;pid=19239" target="_blank">cimetidine</a> (Tagamet HB®) and <a href="http://www.veterinarypartner.com/Content.plx?P=A&amp;S=0&amp;C=0&amp;A=620&amp;EVetID=900000083" target="_blank">famotidine</a> (Pepcid AC®). These medications are not useful in combating allergic symptoms where histamine causes its unpleasant effects by binding so-called H1 receptors; instead they bind to histamine receptors in the stomach called H2 receptors and control stomach acid secretion.</p>
<p>Cimetidine was the first such H2 blocker available and each generation has brought about improvements in terms of fewer drug interactions and stronger effect. Ranitidine offers an additional benefit in that not only does it act as an effective antacid but it also aids in encouraging normal stomach contractions. When it comes to managing nausea, normal stomach contractions are crucial in preventing food from pooling in the stomach. Stomach distension is a stimulus for vomiting. Ranitidine helps with nausea by addressing both stomach acid and stomach contractions. As an antacid, ranitidine is between three and 13 times stronger than its predecessor cimetidine.</p>
<p><strong>How this Medication is Used</strong></p>
<p>Ranitidine is useful in any situation where stomach irritation is an issue and ulceration is a concern. It is often used in the treatment of <a href="http://www.veterinarypartner.com/Content.plx?P=A&amp;S=0&amp;C=0&amp;A=594&amp;EVetID=900000083" target="_blank">Helicobacter infection</a>, <a href="http://www.veterinarypartner.com/Content.plx?P=A&amp;S=0&amp;C=0&amp;A=598&amp;EVetID=900000083" target="_blank">inflammatory bowel disease</a>, <a href="http://www.veterinarypartner.com/Content.plx?P=A&amp;S=0&amp;C=0&amp;A=1199&amp;EVetID=900000083" target="_blank">canine parvovirus</a>, kidney failure, ingestion of a toxin that could be ulcerating (an over dose of <a href="http://www.veterinarypartner.com/Content.plx?P=A&amp;S=0&amp;C=0&amp;A=552&amp;EVetID=900000083" target="_blank">aspirin</a>, for example), any disease involving protracted vomiting, or used chronically in combination with medications that may have stomach irritating properties.</p>
<p>In diseases involving frequent vomiting or regurgitation, the esophagus (tube connecting the mouth and stomach) can be ulcerated by continuing exposure to vomit/stomach acid. Antacids are also helpful in this type of situation to reduce damage to the esophagus.&nbsp;</p>
<p>Ranitidine is generally used twice a day and can be used long term.</p>
<p><strong>Side Effects</strong></p>
<p>The H2 blockers as a group have a limited potential for side effects, hence their recent release to over the counter status.</p>
<p>There have been some reports of exacerbating heart rhythm problems in patients who already have heart rhythm problems so it may be prudent to choose another means of stomach acid control in heart patients.</p>
<p>Occasionally humans report headache or mental confusion.</p>
<p><strong>Interactions with other Drugs</strong></p>
<p>There are some drugs that are absorbed better in the presence of stomach acid, such as <a href="http://www.veterinarypartner.com/Content.plx?P=A&amp;S=0&amp;C=0&amp;A=520&amp;EVetID=900000083" target="_blank">itraconazole</a> or <a href="http://www.veterinarypartner.com/Content.plx?P=A&amp;S=0&amp;C=0&amp;A=521&amp;EVetID=900000083" target="_blank">ketoconazole</a>. The dose may require adjustment when used with ranitidine.</p>
<p><strong>Concerns and Cautions</strong></p>
<p>•&nbsp;The dose of ranitidine may require reduction in patients with liver or kidney disease as these diseases tend to prolong drug activities.<br>&nbsp; <br>•&nbsp;It appears that ranitidine is safe for use in pregnancy but should probably be avoided during lactation.<br>&nbsp; <br>•&nbsp;The use of ranitidine can cause a urine dipstick to falsely test positive for protein.</p>
