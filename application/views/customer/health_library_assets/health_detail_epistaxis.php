<p><strong>Cruising Towards Anesthesia</strong></p>
<p>If the basic blood tests and clotting parameters are normal, then the chances are that the problem is localized to the nose but there are a few more tests that are required before the patient is anesthetized for a nasal examination.</p>
<ul>
<li>Radiographs of the chest should be performed to rule out obvious cancer spread or obvious disseminated fungal disease.</li>
<li>An oral examination should be performed as best as possible. Dental disease can be bad enough to create nasal bleeding, given that the roots of larger teeth connect with the nasal cavity. Oral tumors that have eroded into the nasal cavity may be evident if one can get a good look in the mouth. Many patients will not allow much oral exam and certainly probing the gums and getting a thorough inspection will require anesthesia but it is absolutely worth looking for obvious lesions if it is possible to do so.</li>
</ul>
<p><strong>Diagnostics Requiring General Anesthesia</strong></p>
<div class="HideFromPerl" style="float: right;"><img style="margin: 5px 5px; width: 290px; height: 244px;" src="<?php echo site_url() ?>assets/images/library/4128899.jpg" documentid="0" subdocfilerepositoryid="17979071" fileid="4128899" tabletype="20" width="291" height="243"></div>
<p>If nothing has been revealed by the preceding tests, it is now time for radiographs of the nose, superficial rhinoscopy, and a dental inspection all of which require general anesthesia. Radiographs generally start the procedure as the other procedures might alter the radiographic appearance of tissues. The radiographs help evaluate the tooth roots and sinuses. Nasal tumors are common causes of nosebleeds in elderly dogs and the bone destruction they cause is evident on radiographs. Referral for more advanced imaging such as CT scanning or MRI, may be needed to determine the extent of bone destruction or to clarify radiography findings.</p>
<p>An otoscope (the same gadget used to look in your petâ€™s ears) can be used to look inside the nasal cavity superficially to remove foreign bodies lodged there. Deeper peeking requires an actual endoscope which may not be readily available in general practice.</p>
<p>The teeth can be cleaned under anesthesia with specific attention to the tooth roots (remember, an abscessed upper tooth root penetrates into the nasal sinus above.</p>
<p>If it seems appropriate to do so, some nasal discharge can be flushed through the nose and into a gauze sponge packing the throat. This may be helpful in identifying infectious organisms but may initiate more bleeding so some judgment is required on whether the benefit is worth the risk.</p>
<p><strong>Then What?</strong></p>
<p>If the simple tools of general practice do not reveal adequate information, referral for endoscopy may be needed. Deeper visualization of the nasal tissues is possible with this equipment, plus biopsy specimens can be taken although bleeding is the chief risk. Taking a biopsy is particularly difficult in the nose, not just because of the hemorrhage but because nasal tumors are surrounded by so much inflammation it is difficult to get a representative sample. Often it is not possible to see the area being biopsied directly, especially if prior sampling has led to bleeding.</p>
<p>If radiographs are diagnostic for cancer and aggressive therapy is contemplated, prognosis is highly dependent on the type of tumor so biopsy becomes especially important in this situation.</p>
<p><strong>And Then What?</strong></p>
<p>At the end of all these procedures, sometimes the area of bleeding is simply not accessible without surgery. This would be the final and most invasive procedure in retrieving a difficult foreign body or tissue sample. Extensive bleeding is expected and this is generally the last resort after a long road of diagnostics.</p>
<p>In a study by Bissett et al published in the December 15, 2007, issue of the <em>Journal of the American Veterinary Medical Association</em>, 176 cases of dogs with bloody noses were reviewed to determine which underlying causes were most common. Of these 176 dogs, a definite underlying cause was found in 115 cases.</p>
<ul>
<li>30% had nasal tumors</li>
<li>29% had trauma</li>
<li>17% had nasal inflammation of unknown cause (idiopathic rhinitis)</li>
<li>10% had low platelets</li>
<li>3% had some other blood clotting abnormality</li>
<li>2% had high blood pressure</li>
<li>2% had tooth abscess</li>
</ul>
<p>Conspicuously absent is the nasal fungal infection but since these are frequently regional in nature in dogs, the population studied may not have been in an area where fungi are common pathogens.</p>
<p><strong>Other Relevant Studies</strong></p>
<p><em>Evaluation of factors associated with survival in dogs with untreated nasal carcinoma: 139 cases (1993-2003) by Rassnick et al published in the Journal of the American Veterinary Medical Association&nbsp; 229:401-406, 2006.</em></p>
<p>This study reviewed the outcomes of 139 dogs diagnosed with nasal carcinoma. Since many dogs are euthanized at the time of this diagnosis, this study included only dogs that were alive seven days after the initial diagnosis was made. Dogs studied received only pain medication, anti-inflammatories and antibiotics (no surgery, chemotherapy or radiation therapy). The following statistical findings came out:</p>
<ul>
<li>80% of dogs were purebred. The median age was 11 years and the median body weight was 48 lbs.</li>
<li>77% had nose bleeds. Median survival time for dogs with nosebleeds was 88 days vs. 224 days for dogs with carcinomas that did not have nose bleeds.</li>
<li>Approximately half of the patients studied were felt to have improvement with the supportive care described above.</li>
</ul>
