<p>Some blood-tinged droplets sneezed on the floor might be the only sign or there might be a steady inexorable bloody drip from one or both nostrils. These findings are alarming as well as messy in the home and we want to identify the cause and take care of it promptly if it is possible to do so. The problem is that there are many causes and not all of them are localized to the nose and many are very serious diseases. The following is a review of tests typically necessary to get to the bottom of the bloody nose as well as the conditions that might be responsible.</p>
<p><strong>First Aid</strong></p>
<p>So, you are at home with your pet and a bloody nose starts and does not seem to be stopping. Here are some tips to get the bleeding controlled in the time prior to your vet appointment:</p>
<ul>
<li>Keep yourself calm. If your pet sees you getting frantic, your pet will get frantic too. Excitement = higher blood pressure = more bleeding.</li>
<li>Get an ice pack and apply it to the bridge of the nose (obviously, be sure your pet can breathe around the ice pack). The cold will constrict small blood vessels, which will slow the bleeding.</li>
<li>Do not attempt to insert absorbent material&nbsp;or cotton swabs in the pet's nose as this will generate sneezing, which will make the bleeding worse. A dose of an&nbsp;oxymetazoline nasal spray such as Afrin may help constrict blood vessels and lead to relief.</li>
<li>If the pet has a condition that involves recurring nose bleeds, consider the oral use of the Chinese herb Yunnan Baiyo, which promotes blood clotting tendency. Ask your veterinarian for details.</li>
</ul>
<p style="text-align: center;"><strong>If these steps do not stop the bleeding or the pet is having difficulty breathing, <br>go to your vet's office or local emergency clinic at once.</strong></p>
<p style="text-align: center;"><strong>Don't&nbsp;forget that a pet with a bloody nose will likely swallow a great deal of the draining blood. <br>This may lead to an especially black stool or even vomit with blood clots in it. <br></strong><strong>After a bloody nose, such findings are usually just a reflection of the bloody nose <br>and do not necessarily indicate bleeding in the GI tract.</strong></p>
<p style="text-align: center;">
</p><p><strong>Information your Veterinarian Will Need</strong></p>
<p>You can help your veterinarian tremendously by taking some time to think about the following information and bringing up anything pertinent.</p>
<ul>
<li>Does your pet take medication? Non-steroidal anti-inflammatory medications (aspirin in particular) will inactivate blood clotting factors. Do not assume your vet knows all the medications your pet is taking; list them for your vet.</li>
<li>Do you have any rat poison or has your pet been consuming any dead rodents that might have been poisoned? Most rat poisons act by disabling the ability to clot blood.</li>
<li>Look closely at your pet's face. Is there any deformity or asymmetry? Is the bridge of the nose swollen? Are either of the third eyelids elevated? Does one eye seem to protrude more. does one eye tear more? Does the&nbsp;nose leather (textured tip) look normal?</li>
<li>Could there have been any trauma to the nose? Does your pet play roughly with another animal?</li>
<li>Is your pet exposed to foxtails or other grass awns that could become lodged in the nose?</li>
<li>Has your pet been sneezing? Has the pet been rubbing at the nose?</li>
<li>Open your pet's mouth if possible. Look at the gums under the lips. Is there blood in the mouth? Do the gums seem pale? If they are, this suggests a serious loss of blood and you may have an emergency on your hands.</li>
<li>Is there any evidence of bleeding anywhere besides the nose? You may see a black tarry stool with Intestinal bleeding may present with a black tarry stool. Any unusual bruising should be reported. Any unexplained swelling that might&nbsp;be bleeding under the skin should also be noted.</li>
<li>Is this the first nosebleed or have there been others?</li>
<li>Is the blood coming from both nostrils or only one?</li>
</ul>
<p><strong>Where to Start</strong></p>
<p>After&nbsp;the veterinarian performs a general examination of your pet, some more specific tests are needed with the idea of prioritizing the most likely conditions and least invasive forms of testing.</p>
<p><strong>Blood Tests First</strong></p>
<p>A basic blood panel and urinalysis will probably be needed as a database for the animalâ€™s health as well as to assess the degree of blood loss. This information also serves as a pre-anesthetic evaluation should rhinoscopy or nasal imaging become necessary. A platelet (a blood cell involved in blood clotting) count will be needed as will coagulation tests (common tests are the PT or prothrombin time; the PTT or partial thromboplastin time; the ACT or activated clotting time; and the buccal bleeding or symplate time.) These tests evaluate a complicated biochemical cascade responsible for clotting blood. The pattern of abnormalities found in these tests will sort out blood clotting disorders.</p>
<p>Other blood tests that may be helpful involve titers for fungal infections, a classic cause of the nosebleed. Fungi are inhaled and if the patient is immune-compromised or excessively exposed, the fungus can take root and begin to grow in the nasal cavity.</p>
<p>In cats, the most common nasal fungal infection is caused by <em>Cryptococcus neoformans</em>. The good news here is that a blood test for fungal antigen is very accurate. Any positive number is significant and warrants treatment.</p>
<p>In dogs, fungal infections are not so simple. The most common organisms are <em><a href="https://veterinarypartner.vin.com/doc/?id=4952634&amp;pid=19239" target="_blank">Aspergillus fumigatus</a></em>&nbsp;and <em>Penicillium</em> species. Blood tests are not as accurate especially since there are other species of Aspergillus besides fumigatus and each requires its own blood test. Complicating matters is the fact that nasal tumors predispose a dog to fungal infections so a dog can easily have both problems in the same nose. Blood tests for fungal infections may be included in the initial battery of tests. A negative Aspergillus test does not rule out Aspergillus infection.</p>
<p>Another condition worth mentioning is hyperviscosity syndrome. In this situation, an extremely high blood protein level makes the blood so thick that blood vessels break from the pressure. Certain types of cancer (multiple myeloma, <a href="https://veterinarypartner.vin.com/doc/?id=4951455&amp;pid=19239" target="_self">lymphoma</a>, and certain types of leukemia) as well as infection with <em><a href="https://veterinarypartner.vin.com/doc/?id=4952341&amp;pid=19239" target="_blank">Ehrlichia canis</a>,</em> a blood parasite can cause this syndrome.</p>
<p>A routine blood panel should show the unusual globulin levels that typify hyperviscosity syndrome.</p>
<p>Another relatively simple parameter to measure is blood pressure. <a href="https://veterinarypartner.vin.com/doc/?id=4951756&amp;pid=19239" target="_blank">High blood pressure</a>&nbsp;can occur as a complication of numerous diseases. When blood pressure rises, small blood vessels begin to burst and bleed, not just in the nose but often in the eyes or nervous system as well. Do not be surprised if your veterinarian checks for retinal hemorrhage.</p>
<p>Tick-borne infections (<em>Ehrlichia, <a href="https://veterinarypartner.vin.com/doc/?id=4952053&amp;pid=19239" target="_blank">Babesia</a></em>, and others) commonly involve low platelet counts. Platelets are blood cells involved in clotting and when they become infected with blood parasites, they do not work properly in the clotting cascade. Tick panels are blood panels that screen for infection with numerous tick-borne parasites, most of which can be managed or eradicated with antibiotics.</p>
<p>The bottom line is that there are many causes of nose bleeds but many can be ruled out with non-invasive testing and it is the non-invasive tests that we want to perform first.</p>
