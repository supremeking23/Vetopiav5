<p><strong>Brand Names: Sporonox, Onmel, and Itrafungol</strong></p>
<p>Available in 100 mg capsules or as oral solution</p>
<p><span class="SubHeading2">Background</span></p>
<p>The development of oral medications to be used in the treatment of invasive fungal infections has represented an immense medical breakthrough. With oral treatment available, human patients no longer require hospitalization several days per week for intravenous treatment of their disease; a more normal and productive lifestyle is now possible. Furthermore, the toxicity profiles of the newer oral drugs are vastly better that those of the injectables.</p>
<p><a href="https://veterinarypartner.vin.com/doc/?id=4951403&amp;pid=19239" target="_blank">Ketoconazole</a>&nbsp;was the first such oral antifungal drug but it had room for improvement regarding its side effect potential. There were problems with nausea, liver toxicity, and feminization of male patients. Itraconazole was developed in answer to these concerns.&nbsp;Its potential for side effects is far lower, although its expense, unfortunately, has been problematic.</p>
<p><span class="SubHeading2"><strong>How This Medication Works</strong></span></p>
<p>Itraconazole works by inhibiting the fungal enzymes that produce ergosterol, an important component of the fungal cell wall. Without adequate ergosterol, the fungal cell becomes weak, leaky, and ultimately dies.</p>
<p>Fungal infections for which itraconazole can be used include:</p>
<ul>
<li><a href="ttps://veterinarypartner.vin.com/doc/?id=4951439&amp;pid=19239" target="_blank">Dermatophytosis (ringworm)</a></li>
<li><em><a href="https://veterinarypartner.vin.com/doc/?id=4951356&amp;pid=19239" target="_blank">Malassezia (yeast dermatitis)</a></em></li>
<li><a href="https://veterinarypartner.vin.com/doc/?id=4952436&amp;pid=19239" target="_blank">Blastomycosis</a></li>
<li>Cryptococcosis (window washer's disease)</li>
<li>Histoplasmosis</li>
<li><a href="https://veterinarypartner.vin.com/doc/?id=4952634&amp;pid=19239" target="_blank">Aspergillosis</a></li>
<li>Candidiasis</li>
<li><a href="https://veterinarypartner.vin.com/doc/?id=4952695&amp;pid=19239" target="_blank">Coccidioidomycosis</a> (Valley fever)</li>
</ul>
<p><span class="SubHeading2">Side Effects</span></p>
<p>The chief reason for choosing itraconazole over other antifungal agents is to avoid side effects over the long treatment courses that are typically needed for the treatment of fungal disease.&nbsp; While itraconazole users do not commonly experience side effects, it is important to be aware of what to watch for; the main issue of concern being liver toxicity.</p>
<p>Side effects of&nbsp;note are appetite loss, vomiting, and/or diarrhea. If they occur, medication should be discontinued and liver enzymes should be checked. Appetite loss in particular is felt to be an important sign of toxicity and generally does not occur until itraconazole has been in use for over one month.</p>
<p>If an adverse side effect occurs, it is expected to resolve with discontinuing the&nbsp;medication. After recovery, itraconazole can usually be restarted at a lower dose.</p>
<p><strong>If you think your pet may be having an adverse drug reaction, notify your veterinarian right away.</strong></p>
<p>Itraconazole should not be given to patients with known liver disease if possible. In one research publication, an SGPT/ALT enzyme level of 250 IU/L was felt to be the highest tolerable level compatible with itraconazole use. Approximately five to 10% of dogs receiving standard doses of itraconazole will develop liver disease severe enough to warrant discontinuing the medication. Expect periodic evaluation of liver enzymes (blood testing) to be recommended throughout the duration of itraconazole therapy.</p>
<p>Another side effect worth mentioning is necroulcerative dermatitis, which occurs in 5-10% of dogs receiving itraconazole on the higher end of the recommended dosage spectrum. This reaction leads to a blood vessel inflammation&nbsp;that in turn leads to skin ulcers. Discontinuing itraconazole leads to resolution of this condition, should it arise.</p>
<p><span class="SubHeading2">Interactions With Other Drugs</span></p>
<p>The use of itraconazole in combination with the antacids <a href="https://veterinarypartner.vin.com/doc/?id=4951496&amp;pid=19239" target="_blank">Pepcid</a>, <a href="https://veterinarypartner.vin.com/doc/?id=4952425&amp;pid=19239" target="_blank">Zantac</a>, <a href="https://veterinarypartner.vin.com/doc/?id=7391380&amp;pid=19239" target="_blank">cimetidine</a> (Tagamet), <a href="ttps://veterinarypartner.vin.com/doc/?id=4952494&amp;pid=19239" target="_blank">omeprazole</a> (Prilosec), or with the antibiotic rifampin, will lead itraconazole to be less effective. If itraconazole must be used with these medications, it is recommended that at least&nbsp;two hours pass between giving the antacid and the itraconazole.</p>
<p>Serious heart abnormalities have resulted when itraconazole was used with the antihistamine terfenadine (Seldane) or with the intestinal motility modifier <a href="https://veterinarypartner.vin.com/doc/?id=4952275&amp;pid=19239" target="_blank">cisapride</a> (Propulsid).&nbsp;These medications should not be used with itraconazole.</p>
<p>The following heart medicines will be stronger when used with itraconazole and dosage may need to be reduced: <a href="https://veterinarypartner.vin.com/doc/?id=4951755&amp;pid=19239" target="_blank">amlodipine</a>, digoxin, warfarin, or quinidine. A similar problem occurs with <a href="https://veterinarypartner.vin.com/doc/?id=4952029&amp;pid=19239" target="_blank">cyclosporine</a>, an immune suppressant used in immune-mediated disease, allergy, or after an organ transplant. In fact, there is a long list of medications that are made stronger - and potentially more toxic - when combined with itraconazole. Other drugs include: <a href="https://veterinarypartner.vin.com/doc/?id=4951423&amp;pid=19239" target="_blank">glipizide</a>, a treatment for diabetic cats; the benzodiazepine tranquilizers, such as&nbsp;<a href="https://veterinarypartner.vin.com/doc/?id=4951422&amp;pid=19239" target="_blank">diazepam</a> and <a href="https://veterinarypartner.vin.com/doc/?id=4952652&amp;pid=19239" target="_blank">alprazolam</a>; anti-anxiety medications&nbsp;<a href="https://veterinarypartner.vin.com/doc/?id=4951817&amp;pid=19239" target="_blank">buspirone</a> and <a href="https://veterinarypartner.vin.com/doc/?id=4951974&amp;pid=19239" target="_blank">clomipramine</a>; <a href="https://veterinarypartner.vin.com/doc/?id=4951416&amp;pid=19239" target="_blank">fentanyl</a>, a narcotic pain reliever; and <a href="https://veterinarypartner.vin.com/doc/?id=4951896&amp;pid=19239" target="_blank">cyclophosphamide</a>, a chemotherapy agent. Because of this long list of interactions, it is important not to combine any medication with itraconazole without first checking with your veterinarian.</p>
<p>The macrolide antibiotics, such as <a href="https://veterinarypartner.vin.com/doc/?id=4952501&amp;pid=19239" target="_blank">erythromycin</a>, actually boost itraconazole levels and could increase the potential for itraconazole toxicity.</p>
<div style="background-color: #f0f0f3; padding: 8px;">
<p><strong>Concerns and Cautions</strong></p>
<p><strong><span style="color: #000000;">Itraconazole <span style="text-decoration: underline;">capsules </span>work best when given with food.</span></strong></p>
<p><strong><span style="color: #000000;">Itraconazole suspension (<span style="text-decoration: underline;">liquid</span>) works best given on an empty stomach.</span></strong></p>
</div>
<p>The 100 mg itraconazole capsule is an inconvenient size for most animal patients. For this reason, it is common to have a&nbsp;<a href="https://veterinarypartner.vin.com/doc/?id=4951427&amp;pid=19239" target="_blank">compounding pharmacy</a> make up a prescription as either capsules or flavored liquid in a size that fits the individual patient. One important caveat regarding going through a compounding pharmacy for itraconazole regards the materials used to make up formulations. Compounded itraconazole can be made up from the prescription product (either brand name or generic) or it can be made up from itraconazole available in bulk, which is usually much less costly.&nbsp;The bulk substance is not recommended as it is not as stable as the prescription capsules nor is it absorbed into the patient's body as effectively.&nbsp; The capsules (but not the bulk powder)&nbsp;use special itraconazole-coated beads that are responsible for proper uptake of the drug. The compounding pharmacy must keep the beads intact&nbsp;while making their formulations.</p>
<p>There is an oral liquid version of SporonoxÂ® available in the U.S. however it is of a relatively low concentration which means the dosing volumes needed even for a small cat would be somewhat inconveniently large.&nbsp; More recently (November 2016), a new oral liquid product called "Itrafungol" has been approved for feline use in the U.S. Its introduction will hopefully alleviate some of the issues that have been problematic with compounding.</p>
<p>Itraconazole is able to penetrate most body tissues and thus fight fungal infections in most organs. It cannot penetrate, however, the blood/brain barrier and thus is useless in central nervous system fungal infections. In such a situation, another closely related medication&nbsp;<a href="https://veterinarypartner.vin.com/doc/?id=4952373&amp;pid=19239" target="_blank">fluconazole</a> would be helpful.</p>
<p><strong><span style="color: black;">Fungal infections as a general rule are resistant to treatment and often months of therapy are needed. It is important not to discontinue treatment prematurely despite expense.</span></strong></p>
<p><span style="color: red; font-family: &#39;Times New Roman&#39;; font-size: 12pt; mso-fareast-font-family: &#39;Times New Roman&#39;; mso-ansi-language: EN-US; mso-fareast-language: EN-US; mso-bidi-language: AR-SA;"></span><span style="color: red; font-family: &#39;Times New Roman&#39;; font-size: 12pt; mso-fareast-font-family: &#39;Times New Roman&#39;; mso-ansi-language: EN-US; mso-fareast-language: EN-US; mso-bidi-language: AR-SA;"></span>(An exception might be a&nbsp;<a href="https://veterinarypartner.vin.com/doc/?id=4951356&amp;pid=19239" target="_self"><em>Malassezia</em> (yeast dermatitis)</a>, which frequently clears up in a couple of weeks.)</p>
<p>Itraconazole is dangerous if given&nbsp;during pregnancy. If a pet is pregnant, itraconazole should be avoided unless the fungal infection in question is potentially life-threatening.</p>
<p>Itraconazole should also be avoided&nbsp;during lactation as it will be delivered via milk to any nursing young.</p>
<p>Itraconazole can reduce the strength of the heart's contractions to an extent that would not be significant in the absence of heart disease. This could, however, be a problem in a patient with reduced heart function.</p>
<p>It is important to give itraconazole with food for best absorption into the body.</p>
