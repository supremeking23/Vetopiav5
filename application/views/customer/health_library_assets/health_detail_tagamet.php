<p><strong>Brand Name: Tagamet</strong></p>
<p>Available in 200 mg, 300 mg, 400 mg, and 800 mg tablets and oral suspension (liquid)</p>
<p><strong>Background</strong></p>
<p>Stomach ulceration in humans is a prominent medical condition and there has long been pressure to develop effective and convenient ways to control it. Until relatively recently, we relied on simply neutralizing stomach acid by pouring alkaline solutions (i.e., Alka Seltzer, Tums, Rolaids etc.) into the stomach. In fact, ulceration is a complicated process and there are many ways to address it.</p>
<p>Control of stomach acidity is a very important factor in the treatment of stomach ulcers. Acid secretion is controlled by a hormone called gastrin (secreted in the presence of food and leading to secretion of stomach acid), acetylcholine (a neurotransmitter), and histamine (that same substance responsible for the unpleasant allergic effects of hay fever).</p>
<p>Cimetidine is an antihistamine as are its cousins famotidine (Pepcid AC®) and ranitidine (Zantac®).&nbsp;We normally think of antihistamines as treatments for allergy but, in fact, there are two different types of histamine receptors: H1 (the kind involved in allergy) and H2 receptors (the kind in the stomach). Cimetidine, ranitidine, and famotidine bind to histamine receptors in the stomach which prevents histamine from causing stomach acid secretion.</p>
<p>Cimetidine was the first such H2 blocker available and each generation has brought about improvements in terms of fewer drug interactions and stronger effect. Famotidine is the longest lasting of the H2 blockers (usually one dose lasts 24 hours). Famotidine is 32 times stronger in its ability to inhibit stomach acid than is cimetidine and is&nbsp;nine times stronger than ranitidine. A newer H2 blocker called nizatidine is now available which offers the additional advantages of especially rapid onset of action and some effect on normalizing stomach contractions as well. These newer products have pushed cimetidine to the background but it is still effective and still available.</p>
<p>Cimetidine is currently available in an over-the-counter formulation, making it highly convenient for pet owners to obtain (though obviously&nbsp;you should not consider using medications licensed for human consumption without specific instructions from one's veterinarian). Cimetidine is especially useful for pets with chronic vomiting though some of the newer antacids are have a more convenient dosing scheduled. Cimetidine has much more potential for drug interactions than its newer cousins but if&nbsp;you&nbsp;are aware of what those are, problems should be avoidable.</p>
<p><strong>How this Medication is Used</strong></p>
<p>Cimetidine is useful in any situation where stomach irritation is an issue and ulceration is a concern. It is often used to treat <em><a href="https://veterinarypartner.vin.com/doc/?id=4951472&amp;pid=19239" target="_blank">Helicobacter</a> </em>infection, <a href="https://veterinarypartner.vin.com/doc/?id=4951476&amp;pid=19239" target="_blank">inflammatory bowel disease</a>, <a href="https://veterinarypartner.vin.com/doc/?id=4951754&amp;pid=19239" target="_blank">canine parvovirus</a>, after ingestion of a toxin that could be ulcerating (overdose of aspirin, for example), any disease involving protracted vomiting, or chronically in combination with other medications which may have stomach irritating properties.</p>
<p>In diseases involving frequent vomiting or regurgitation, the esophagus (tube connecting the mouth and stomach) can be ulcerated by continuing exposure to vomit/stomach acid. Antacids are also helpful in this type of situation to reduce damage to the esophagus. <a href="https://veterinarypartner.vin.com/doc/?id=4951482&amp;pid=19239" target="_blank">Megaesophagus</a> would be a condition where an antacid such as cimetidine could be helpful in mitigating injury to the esophagus though there is a trade off in protection against aspiration pneumonia.</p>
<p>Cimetidine is directly helpful in managing nausea in species where there are H1 receptors in the brain's chemoreceptor trigger zone (an area involved in stimulating the act of vomiting). In other words, famotidine is not only an antacid but also an antinauseal for dogs but is only an antacid for cats.</p>
<p><strong>Side Effects</strong></p>
<p>The H2 blockers as a group have a very limited potential for side effects, hence their recent release to over-the-counter status.</p>
<p>There have been some reports of exacerbating heart rhythm problems in patients who already have heart rhythm problems so it may be prudent to choose another means of stomach acid control in heart patients.</p>
<p><strong>Interactions with other Drugs</strong></p>
<p>There are some drugs that are absorbed better in with stomach acid (examples: <a href="https://veterinarypartner.vin.com/doc/?id=4951402&amp;pid=19239" target="_blank">itraconazole</a>, <a href="https://veterinarypartner.vin.com/doc/?id=4952373&amp;pid=19239" target="_blank">fluconazole</a>, <a href="https://veterinarypartner.vin.com/doc/?id=4951403&amp;pid=19239" target="_blank">ketoconazole</a>). These drugs should be given at least two&nbsp;hours apart from cimetidine.</p>
<p>Cimetidine is best absorbed in the presence of stomach acid so if other antacids are used, they should be staggered at least&nbsp;two hours from the cimetidine dose.<br>The following drugs are metabolized more slowly in the presence of cimetidine (which means they will last longer or be stronger):</p>
<ul>
<li>Benzodiazepine tranquilizers (such as <a href="https://veterinarypartner.vin.com/doc/?id=4951422&amp;pid=19239" target="_blank">diazepam</a> and alprazolam).</li>
<li><a href="https://veterinarypartner.vin.com/doc/?id=4951500&amp;pid=19239" target="_blank">Chloramphenicol</a> (an antibiotic)</li>
<li><a href="https://veterinarypartner.vin.com/doc/?id=4951414&amp;pid=19239" target="_blank">Metronidazole</a> (an antibiotic)</li>
<li><a href="https://veterinarypartner.vin.com/doc/?id=4951421&amp;pid=19239" target="_blank">Theophylline</a> (an airway dilator)</li>
<li>Beta blockers (used in heart disease)</li>
<li>Calcium channel blockers (used in heart disease)</li>
<li>Tricyclic antidepressants</li>
</ul>
<p><strong>Concerns and Cautions</strong></p>
<p>The dose of cimetidine may need to be lowered in patients with liver or kidney disease as these diseases tend to prolong drug activities. Humans with liver or kidney issues or simply elderly people report mental confusion with cimetidine. Headaches have also been reported as a human side effect.</p>
<p>Cimetidine should be given on an empty stomach for best effect.</p>
<p>Cimetidine can alter skin testing for allergen specific immunotherapy.&nbsp; Your veterinarian will need to know what medications your pet is one before this kind of testing as many medications can alter results.</p>
<p>Cimetidine must be given&nbsp;two to four&nbsp;times daily. Other H2 blockers may be more convenient in their dosing schedule.</p>
<p>Cefpodoxime may be a problem for animals with seizure disorders. If another antibiotic will suffice, it may be best to choose something other than cefpodoxime for such patients.</p>
<p>It is our policy not to give dosing information over the Internet.</p>
