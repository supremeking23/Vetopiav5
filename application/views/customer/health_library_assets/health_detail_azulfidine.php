<p><strong>Brand Name: Azulfidine</strong></p>
<p>Available in 500 mg tablets&nbsp;</p>
<p><strong>Background</strong></p>
<p><a href="<?php site_url()?>customer/health_detail/26" target="_blank">Colitis</a> is another&nbsp;word for inflammation of the colon (large intestine). Clinical signs of colitis&nbsp;include bloody or mucous diarrhea with urgency and sometimes vomiting. Since the colon is the last portion of the intestinal tract (following the mouth, esophagus, stomach, and small intestine), medications must reach the colon in one of two ways. They must either resist digestion all the way down until they get to their final destination in the colon or they must be absorbed into the body earlier in the digestion process, enter the bloodstream, and reach the colon through its blood supply. Sulfasalazine&nbsp;uses the former mechanism.</p>
<p>Sulfasalazine is created by bonding a sulfa antibiotic called sulfapyridine to a salt of salicylic acid (<a href="http://www.veterinarypartner.com/Content.plx?P=A&amp;S=0&amp;C=0&amp;A=552&amp;EVetID=900000083" target="_blank">aspirin</a>). The bond is intact all the way down the intestinal tract until it is cleaved in the colon by resident bacteria releasing the two component molecules. The sulfa antibiotic is absorbed into the body and has minimal effect but the salicylic acid exerts its famous anti-inflammatory effects locally. Essentially, a salve is applied to the surface of the inflamed colon.</p>
<p><strong>How this Medication is Used</strong></p>
<p>Sulfasalazine is given as a tablet three times daily, which makes it somewhat less convenient to use than other medications commonly used for colitis (<a href="http://www.veterinarypartner.com/Content.plx?P=A&amp;S=0&amp;C=0&amp;A=532&amp;EVetID=900000083" target="_blank">metronidazole</a>, <a href="http://www.veterinarypartner.com/Content.plx?P=A&amp;S=0&amp;C=0&amp;A=1329&amp;EVetID=900000083" target="_blank">tylosin</a>). It is usually not given for courses longer than 3 weeks due possible side effects on tear production (see below).</p>
<p><strong>Side Effects</strong></p>
<p>Sulfa-type antibiotics have been associated with several rare but well documented side effect syndromes:</p>
<p><em>Joint inflammation<br></em>A broad inflammatory syndrome has been observed in some individuals sensitive to <a href="http://www.veterinarypartner.com/Content.plx?P=A&amp;S=0&amp;C=0&amp;A=617&amp;EVetID=900000083" target="_blank">trimethoprim sulfa</a> and might be seen with the use of other sulfas. This includes arthritis, fever, muscle soreness, kidney inflammation, and even inflammation in the eye.&nbsp;This syndrome has been formally studied and has been found to occur almost exclusively after a previous uneventful exposure to sulfa and occurs eight to 20 days after therapy has started. Complete recovery can be expected within one week of discontinuing the medication. The Doberman pinscher seems to be over-represented.<br>&nbsp; <br><em>Skin rashes</em><br>Drug-related skin reactions do not have characteristic appearances; in fact, they can have any appearance. They do, however, begin around the start of treatment with the offending drug and vanish&nbsp;when the&nbsp;offending drug is stopped. Any drug of any kind can produce a drug reaction in the skin; sulfa is somewhat over-represented in cases of skin-related drug eruptions.<br>&nbsp; <br><em>Inability to produce adequate tears</em><br>Sulfa drugs of any kind are capable of disrupting tear function. Classically, this occurs after long-term therapy (weeks to months) of use but occasionally certain individuals suffer from dry eyes after only one dose of sulfa. In most cases, tear function resumes normally after the drug is discontinued but occasionally the effect is long term or permanent despite withdrawal of the drug. This is the most common side effect. <br>&nbsp; <br><em>Hepatitis<br></em>Liver failure can result when a sensitive individual receives this medication. Nausea, jaundice, and all the other complications that occur with liver failure of any origin may result. Discontinuing the medication should lead to recovery. If the liver is biopsied during its state of failure, changes associated with sulfa reaction are characteristic (i.e. it should be possible via biopsy to determine if a failing liver was caused by an&nbsp;unanticipated sulfa reaction).<br>&nbsp; <br><em>Blood dyscrasias<br></em>When abnormal blood cells or proportions of different blood cells are created, it's called blood dyscrasia. Blood dyscrasias might lead to immune dysfunction, bleeding tendency, or other problems depending on which blood cells are affected. With sulfa antibiotics, loss of red blood cells, platelets, and white blood cells have been reported. This syndrome is typically part of the joint inflammation syndrome.</p>
<p>It should be noted that the above side effects are a general list of the classical side effects reported with sulfa antibiotics in general; they do not refer to sulfasalazine specifically. The sulfa&nbsp;potion of sulfasalazine demonstrates side effects only rarely with tear production problems being the only one of any likelihood. The others are potential side effects as they have been seen with closely related drugs.</p>
<p><strong>Interactions with other Drugs</strong></p>
<p>Once absorbed into the body, the sulfa&nbsp;portion of sulfasalazine is quickly bound to a protein carrier (i.e. once inside the body, it travels through the circulation coupled with blood proteins.) If the patient is concurrently taking other drugs that rely on protein carriers to travel in the circulation, the sulfa may bump these other drugs off the carrier. Once a drug is bumped off, it is able to act on its target tissue. This means that protein-bound drugs are stronger in patients concurrently taking sulfas. Drugs&nbsp;that may be enhanced in this way include: methotrexate (a cancer chemotherapy agent), warfarin (an anti-coagulant used both therapeutically and as the active ingredient in <a href="http://www.veterinarypartner.com/Content.plx?P=A&amp;S=0&amp;C=0&amp;A=1765&amp;EVetID=900000083" target="_blank">rat poisons</a>), thiazide diuretics, <a href="http://www.veterinarypartner.com/Content.plx?P=A&amp;S=0&amp;C=0&amp;A=552&amp;EVetID=900000083" target="_blank">aspirin</a>, and phenytoin (an anti-seizure drug).</p>
<p>Digoxin (a heart medication), may work less effectively with sulfasalazine.</p>
<p><br><strong>Concerns and Cautions</strong></p>
<p>Cats are sensitive to salicylates. This medication is used in dogs or with dosage adjustments for cats.<br><br>This drug may affect male fertility.</p>
<p>When sulfasalazine has been given to pregnant women, birth defects have occasionally arisen. It is best not to use this medication during pregnancy.</p>
<p><em>It has been noted that the only side effect of reasonable likelihood is decreased tear production. </em><em>It is important to watch for eye squinting or apparent eye discomfort. If this is noted, discontinue the sulfasalazine and inform the veterinarian promptly</em>.</p>
<p>Sulfasalazine is a bright orange-colored medication. If the patient vomits, as colitis patients sometimes do, the orange dye is not easily removed from fabric, including carpeting.</p>
