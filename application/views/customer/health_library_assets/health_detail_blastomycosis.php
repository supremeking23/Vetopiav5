</p><p><img src="<?php echo site_url();?>assets/images/library/4127238.jpg" imgid="4127238" fileid="4127238" tabletype="20" last="true" style="margin: 5px; width: 250px; height: 188px; float: right;" subdocfilerepositoryid="17422168" documentid="7960873" width="251" height="188"></p>
<p>Blastomycosis, caused by <em>Blastomyces dermatitidis</em>, is a systemic fungal infection that affects dogs and cats. Blastomycosis appears in only certain geographic areas in North America, most often the Mississippi, Ohio, Missouri, Tennessee, and St. Lawrence River valleys. (Fungal growth is supported by wet, sandy, acidic soils rich in organic matter, which is why it is found in valleys and is seen most frequently near water.) The disease occurs occasionally in the Middle Atlantic States, three provinces of Canada (Quebec, Ontario, Manitoba), along the southern Great Lakes, and in Central America.</p>
<p>The prognosis for many pets is good, especially if owners can afford to treat the pet for long enough. Approximately 50% to 75% of affected dogs recover when treated with itraconazole or an amphotericin-ketoconazole combination. Prognosis is poor for dogs with severely affected lungs, but if the dog survives the first 7 to 10 days of therapy, the prognosis improves. Dogs with brain involvement often die. Relapses are most likely to occur in dogs that had a severe case at onset, or dogs that were not treated long enough. Relapses are most common within the first 6 months after treatment. Dogs that recover from the disease are probably not immune to getting it again.Infection with <em>Blastomyces</em> occurs when a cat or dog inhales the fungal spores into the lungs. The incubation period is from 5 to 12 weeks. Some animals don't show clinical signs for a long time after being infected, but those animals are not contagious to other animals and people. Once the lung disease develops, yeast forms of the organism spread throughout the body. Organs typically affected include eyes, bones, skin, lymph nodes, subcutaneous tissues, brain, and testes. It can also be found in the nose, prostate, liver, mammary glands, and heart, but those locations are less common.</p>
<p>Dogs appear to be much more susceptible to infection than other species. Although the disease does occur in both people and cats, the incidence is much lower than in dogs. Dogs are ten times more likely to contract the disease than are people, and 100 times more likely than are cats. The incubation period in dogs is also shorter than in people. The reason dogs are more susceptible is unknown, but immune-deficiency may play a role.</p>
<p>The signs of illness will depend on what organs are infected. Some dogs will have eye problems, and some will have neurological signs (seizures, head tilt, etc.). Lameness may be the biggest concern, if the infection is in the bones. Owners of dogs in the specific geographic areas should watch for coughing, difficulty breathing, eye inflammation, enlarged testes, fever, and lack of appetite. (Weight loss occurs because of anorexia.) Large skin abscesses and neurologic signs are more common in cats than in dogs. Bony lesions are more common in dogs.</p>
<p>Diagnosis involves blood tests, radiographs, urinalysis to look for <em>Blastomyces</em> yeast, cultures, serology, and biopsies of affected organs. Fungal serology, to look for antibodies, is not always accurate and has been known to produce false negatives. The enzyme immunoassay (EIA) for <em>B. dermatitidis galactomannan</em> antigen appears to have a high sensitivity in urine (93.5%) and serum (87%). The EIA assay is commercially available; cross reactions with <em>Histoplasma capsulatum, Coccidioides immitis, </em>and<em> Penicillium marneffei</em> can occur with this assay. The definitive diagnosis is finding the fungus in the tissues.</p>
<p>Treatment involves various antifungal medications, including <a href="http://www.veterinarypartner.com/Content.plx?P=A&amp;S=0&amp;C=0&amp;A=520&amp;EVetID=900000083" target="_blank">itraconazole</a>, <a href="http://www.veterinarypartner.com/Content.plx?P=A&amp;S=0&amp;C=0&amp;A=521&amp;EVetID=900000083" target="_blank">ketoconazole</a>, <a href="http://www.veterinarypartner.com/Content.plx?P=A&amp;S=0&amp;C=0&amp;A=2148&amp;EVetID=900000083" target="_blank">fluconazole</a>, amphotericin B, and some combination therapies. These medications usually need to be taken for a long period of time; how long depends on the specific case. Treatment usually can be done at home, unless the disease is severe. Pets with respiratory involvement should be on restricted activity.&nbsp;In severely ill dogs, intravenous fluids, oxygen, and pain medication are used. Pets with severe lung disease may get worse at the beginning of treatment because the fungal organisms are dying; the mass death of organisms can cause severe respiratory problems. Patients may not appear to improve for one to two weeks after the start of treatment. Close moni toring should be done for the first two weeks, and then rechecks are usually scheduled on a monthly basis. Rechecks may involve blood tests, biochemistry profiles, and radiographs, if appropriate.</p>
<p>Eyes that are severely affected may not respond well to the treatment because the medication does not penetrate eyes very well. Significantly affected eyes may require <a href="http://www.veterinarypartner.com/Content.plx?P=A&amp;S=0&amp;C=0&amp;A=569&amp;EVetID=900000083" target="_blank">enucleation</a> (removal of the eyeball).</p>
<p>There is no way to prevent your pets' exposure to <em>Blastomyces</em> other than by keeping them away from affected geographic areas.</p>
<p>It is extremely rare for humans to acquire blastomycosis from an animal. Nearly all human blastomycosis cases are from inhaling spores&nbsp;in the environment.</p>
