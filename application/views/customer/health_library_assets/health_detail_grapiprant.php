<p>Brand Name: Galliprant</p>
<p>Available in 20 mg, 60 mg, and 100 mg tablets</p>
<p>The importance of pain relief cannot be overemphasized. Dogs, especially large breed dogs, commonly suffer from arthritis pain and loss of mobility, especially as they age. The issue can become so extreme that life quality is impaired. Human pain relievers abound but because many are toxic or dangerous to dogs, medications made just for dogs have evolved. The development of canine-friendly non-steroidal anti-inflammatory drugs (<a href="https://veterinarypartner.vin.com/doc/?id=4952627&amp;pid=19239" target="_blank">NSAIDs</a>) created a revolution in pain management for dogs to a point where NSAIDs have become standard components of arthritis pain relief as well as surgery-related analgesia.</p>
<p>One of the limitations of the NSAID group has stemmed from the biochemistry of the prostaglandin suppression that NSAIDs use. NSAIDs work by suppressing prostaglandins. The problem is that we do not want to suppress all prostaglandins, just the ones associated with pain and inflammation. As more sophisticated NSAIDs were developed to suppress only certain pathways, we still found that even prostaglandins typically considered "bad" can still have some good effects. In other words, even the use of the most innovative NSAIDs has been a double-edged sword in certain situations. We only want to suppress pain and inflammation; we don't want to decrease blood flow to the GI tract or kidneys, nor interfere with the ability to clot blood.</p>
<p>Enter the piprant class of anti-flammatory drugs. Grapiprant is the first priprant anti-inflammatory to come market with its release in 2017. Grapiprant does not suppress prostaglandin production; instead, it interferes with a specific prostaglandin receptor called the EP4 receptor.&nbsp;It is&nbsp;the receptor involved in&nbsp;generating pain and inflammation. By interfering with only the EP4 receptor, grapiprant suppresses pain with no effect on the kidney, blood clotting mechanisms, or GI tract. In this way, the side effects and drug reactions that have been concern for patients on NSAIDs should not be concerns with grapiprant.</p>
<p><strong>How this Medication is Used</strong></p>
<p>Grapiprant is used once a day to manage pain in dogs.</p>
<p>Unlike the NSAIDs where pre-screening lab work is important to be sure the patient does not have a kidney or liver condition that could be exacerbated by an NSAID, pre-screening labs are theoretically not necessary with grapiprant. With NSAIDs, periodic monitoring blood tests are recommended to be sure the patient's metabolic situation has not changed and that NSAIDs are still safe. Similar monitoring is still recommended with grapipriant and is prudent in any patient on long term medications.</p>
<p><strong>Side Effects</strong></p>
<p>Most side effects are GI related: diarrhea, appetite loss, vomiting. It is not uncommon for minor intestinal upset to be seen in patients starting grapiprant but symptoms were mild and transient.</p>
<p>Albumin is an important blood protein. It is involved in transporting materials through the body. Albumin levels can be reduced with grapiprant use. This is probably not an important side effect in a normal animal but if a patient already has a disease that lowers blood albumin, it is probably best not to lower albumin further.</p>
<p>Grapiprant is a sulfa-based medication. Sulfa-based medications of other types have been associated with reduced tear production as well as some immune-mediated conditions. No such conditions were seen in any of the test dogs studied prior to release of grapiprant.</p>
<p><strong>Interactions with other Drugs</strong></p>
<p>Grapiprant use in combination with corticosteroids (<a href="https://veterinarypartner.vin.com/doc/?id=4951498&amp;pid=19239" target="_blank">prednisolone</a>, <a href="https://veterinarypartner.vin.com/doc/?id=4951415&amp;pid=19239" target="_blank">dexamethasone</a>, triamcinolone, etc.) has not been studied and the manufacturer recommends against concurrent use of these medications. Similarly, grapiprant should not be used with NSAIDs.</p>
<p>Grapiprant is highly protein-bound and this is a potential problem when it is used with other highly protein-bound medications.</p>
<p>Grapiprant is a highly protein-bound medication. What that means is that in the bloodstream, 95 percent&nbsp;of grapiprant is transported bound to an albumin (carrier protein) molecule while the&nbsp;rest is floating free in the bloodstream. The&nbsp;five percent&nbsp;that is floating free is the active portion. As the free portion is removed from the body by the liver, the blood protein gives up what it is carrying to replace it. This continues until all of the drug has been removed by the liver. Problems can result when other highly protein-bound drugs are used concurrently. The drugs will "fight" over places on the albumin carrier molecule (imagine a bus with only a certain number of seats and too many passengers trying to fill them). The drug with the higher protein affinity will be carried and the other drug is left floating free and active, meaning there will be much more active drug than normal. This can lead to toxicity problems. Grapiprant has not been tested with other drugs in this way so how significant an issue this is remains to be seen.</p>
<p><strong>Concerns and Cautions</strong></p>
<p>Grapiprant should not be given with food. It will not absorb into the body predictably.</p>
<p>Grapiprant is not approved for dogs under&nbsp;nine months of age and cannot be accurately dosed in dogs under eight pounds of body weight.</p>
<p>Grapirant has also not been evaluated in breeding, pregnant, or nursing dogs.</p>
<p>Unlike NSAIDs, grapiprant has no use against fever.</p>
<p>Grapiprant has only been tested in healthy dogs, though in healthy dogs given 15 times the recommended dose for&nbsp;nine months no serious consequences resulted. This represents a broad safety margin.</p>
<p>Given that grapiprant does not interfere with kidney or GI blood flow, it should theoretically be safe to give in dogs with kidney insufficiency or with intestinal disease but theory is still theory. Again, grapiprant has only been tested in healthy dogs.</p>
<p>Grapirant has not been studied in dogs with heart disease. This is important because there are a lot of EP4 receptors in heart tissue. The consequences of this are unknown.</p>
<p>Monitoring blood tests are recommended when grapirant is used long term.</p>
<p><strong>Grapirant is not approved for cats.</strong></p>
