<p style="text-align: center;">
</p><p><strong>How Infection Occurs</strong></p>
<p>An infected dog sheds infectious bacteria and/or viruses in respiratory secretions. These secretions become aerosolized and float in the air where they can be inhaled by a healthy dog. Obviously, crowded housing and suboptimal ventilation play important roles in the likelihood of transmission but organisms may also be transmitted on toys, food bowls or other objects.</p>
<div class="HideFromPerl"><img width="503" height="171" style="margin: 5px auto; display: block;" src="<?php echo site_url()?>assets/images/library/4127487.gif" ></div>
<p>The normal respiratory tract has substantial safeguards against invading infectious agents. The most important of these is probably what is called the mucociliary escalator. This safeguard consists of tiny hair-like structures called cilia that protrude from the cells lining the respiratory tract and extend into a coat of mucus over them. The cilia beat in a coordinated fashion through the lower and more watery mucus layer called the sol. A thicker mucus layer called the gel floats on top of the sol. Debris, including infectious agents, get trapped in the sticky gel and the cilia move them upward towards the throat where the collection of debris and mucus may be coughed up and/or swallowed.</p>
<p>The mucociliary escalator is damaged by the following:</p>
<ul>
<li>shipping stress</li>
<li>crowding stress</li>
<li>heavy dust exposure</li>
<li>cigarette smoke exposure</li>
<li>infectious agents (as listed previously)</li>
<li>cold temperature</li>
<li>poor ventilation.</li>
</ul>
<p>Without this, a fully functional mucociliary escalator or invading bacteria, especially <em>Bordetella bronchiseptica</em>, the chief agent of kennel cough, may simply march down the airways unimpeded.</p>
<p><em>Bordetella bronchiseptica</em> organisms have some tricks of their own as well:</p>
<ul>
<li>They are able to bind directly to cilia, rendering them unable to move within 3 hours of contact.</li>
<li>They secrete substances that disable the immune cells normally responsible for consuming and destroying bacteria.</li>
</ul>
<p>Because it is common for <em>Bordetella</em> to be accompanied by at least one other infectious agent (such as one of the viruses listed below), kennel cough is actually a complex of infections rather than infection by one agent.</p>
<p>Classically, dogs get infected when they are kept in a crowded situation with poor air circulation&nbsp;and lots of warm air (i.e., a boarding kennel, vaccination clinic, obedience class, local park, animal shelter, animal hospital waiting room, or grooming parlor). In reality, most causes of coughing that begin acutely in a dog are due to infectious causes and usually represent some form of kennel cough.</p>
<p><span style="color: #ff0000;"><strong>THE INCUBATION PERIOD IS 2 TO 14 DAYS.&nbsp;<br><br>Dogs are typically sick for 1 to 2 weeks. <br><br>Infected dogs shed Bordetella organism for 2 to 3 months following infection.</strong></span></p>
<p><strong>How is Diagnosis Made?</strong></p>
<p>A coughing dog that has a poor appetite, fever, and/or listlessness should be evaluated for pneumonia.</p>
<p>Usually the history of exposure to a crowd of dogs within the proper time frame, plus typical examination findings (coughing dog that otherwise feels well) is adequate to make the diagnosis. Radiographs show bronchitis and are particularly helpful in determining if there is a complicating pneumonia.</p>
<p>Recently, PCR (polymerase chain reaction) panels have become available in many reference laboratories. Using technology to amplify the presence of DNA in a swab, the lab is able to test for most of the kennel cough infectious agents listed. This knowledge is helpful in guiding therapy and understanding expectations.</p>
<p><strong>How is Kennel Cough Treated?</strong></p>
<p>An uncomplicated case of kennel cough will go away by itself. Cough suppressants can improve patient comfort while the infection is resolving. The dog should be clearly improved, if not recovered, after about a week. That said, several infectious agents in the kennel cough complex are more intense and can cause a minor bronchitis to progress to <a href="http://www.veterinarypartner.com/Content.plx?P=A&amp;S=0&amp;C=0&amp;A=1758&amp;EVetID=900000083" target="_blank">pneumonia</a>, which is a potentially life-threatening disease. Given this possibility, antibiotics are frequently prescribed to kennel cough patients to prevent or curtail pneumonia before it warrants hospitalization.</p>
<p>It is important to distinguish an uncomplicated case of kennel cough from one complicated by pneumonia for obvious reasons. The uncomplicated cases will not have fever or appetite loss and they will not be listless. As mentioned, they will seem normal except for coughing. Dogs with pneumonia appear sick.</p>
<p><strong>Prevention through Vaccination </strong></p>
<p><img width="288" height="194" title="nasal vaccine Brooks" style="margin: 5px; float: left;" src="<?php echo site_url()?>assets/images/library/4345567.jpg" >Vaccination is only available for: <em>Bordetella bronchiseptica</em>, canine adenovirus type 2, canine parainfluenza virus, canine distemper, and canine influenza. Infections with other members of the kennel cough complex cannot be prevented. Vaccine against adenovirus type 2, parainfluenza, and canine distemper is generally included in the basic puppy series and subsequent&nbsp; boosters (the DHPP or&nbsp; distemper-parvo shot).&nbsp; For <em>Bordetella bronchiseptica</em>, vaccination can either be given as a separate injection or as a nasal immunization. There is some controversy regarding which method provides a better immunization or if a combination of both formats is best.</p>
<p><em>Nasal Vaccine</em><br>Intranasal vaccination may be given as early as 3 weeks of age and immunity generally lasts 12 to 13 months. The advantage is that the local immunity is stimulated right at the site where the natural infection would try to take hold.</p>
<p>It takes four days to generate a solid immune response after intranasal vaccination, so it is best if vaccination is given at least four days prior to the exposure. Some dogs will have some sneezing or nasal discharge in the week following intranasal vaccination; this should clear up on its own. As a general rule, nasal vaccination provides faster immunity than injectable vaccination.</p>
<p>Nasal vaccines for <em>Bordetella</em> generally also include vaccine against parainfluenza virus and some also include vaccine against adenovirus type 2.</p>
<p><em>Injectable Vaccine</em><br>Injectable vaccination is a good choice for aggressive dogs who may bite if their muzzle is approached. For puppies, injectable vaccination provides good systemic immunity as long as two doses are given (approximately one month apart) after age 4 months. Boosters are generally given annually. Some dogs experience a small lump under the skin at the injection site. This should resolve without treatment.</p>
<p>VACCINATION IS NOT USEFUL IN A DOG ALREADY INCUBATING KENNEL COUGH.</p>
<p><em>Bordetella bronchiseptica</em> vaccination may not prevent infection. In some cases, vaccination minimizes symptoms of illness but does not entirely prevent infection. This is true whether nasal or injectable vaccine is used.</p>
<p><strong><em>Dogs that have recovered from Bordetella bronchiseptica are typically immune to reinfection for 6 to 12 months.<br></em></strong></p>
<p><strong>What if Kennel Cough doesn't Improve?</strong><br><br>As previously noted, this infection is generally self-limiting. It should be at least improved partially after one week of treatment. If no improvement has been observed in this time, a re-check exam (possibly including radiographs of the chest) would be a good idea. Failure of kennel cough to resolve suggests an underlying condition. Kennel cough can activate a previously asymptomatic collapsing trachea or the condition may have progressed to pneumonia. Alternatively, there may be another disease afoot entirely such as non-infectious bronchitis, congestive heart failure, or some other condition that causes coughing.<br><br>If you have questions about a coughing dog, do not hesitate to bring them to your veterinarian.</p>
