          </p><p><strong>(Also called Canine Infectious Respiratory Disease, CIRD, or Infectious Tracheobronchitis)</strong></p>
<p><strong>What is it?</strong></p>
<div class="HideFromPerl" style="float: right;"><img width="206" height="112" style="margin: 5px; float: right;" src="<?php echo site_url()?>assets/images/library/kennel.gif"></div>
<p><a href="https://www.veterinarypartner.com/vetzinsight/default.aspx?pid=756&amp;catId=5860&amp;Id=8115910" target="_blank">Kennel cough</a> is an infectious bronchitis of dogs characterized by a harsh, hacking cough that most people describe as sounding like “something stuck in my dog’s throat.” This bronchitis may be of brief duration and mild enough to warrant no treatment at all or it may progress all the way to a life-threatening pneumonia depending on which infectious agents are involved and the immunological strength of the patient. An uncomplicated kennel cough runs a course of a week or two and entails frequent fits of coughing in a patient who otherwise feels active and normal. Uncomplicated cases do not involve fever or listlessness, just lots of coughing.<br><br>Numerous organisms may be involved in a case of kennel cough; it would be unusual for only one agent to be involved. Infections with the following organisms frequently occur concurrently to create a case of kennel cough:</p>
<ul>
<li><em>Bordetella bronchiseptica</em> (bacteria)<a href="http://www.veterinarypartner.com/Content.plx?P=A&amp;S=0&amp;C=0&amp;A=1758&amp;EVetID=900000083" target="_blank"></a></li>
<li>Parainfluenza virus</li>
<li>Adenovirus type 2</li>
<li>Canine distemper virus</li>
<li>Canine influenza virus</li>
<li>Canine herpesvirus (very young puppies)</li>
<li><em>Mycoplasma canis</em> (a single-cell organism that is neither virus nor bacterium)</li>
<li>Canine reovirus</li>
<li>Canine respiratory coronavirus.</li>
</ul>
<p>The classical combination for uncomplicated kennel cough is infection with parainfluenza or adenovirus type 2 in combination with <em>Bordetella bronchiseptica</em>. Infections involving the distemper virus, <em>Mycoplasma</em> species, or canine influenza are more prone to progressing to <a href="http://www.veterinarypartner.com/Content.plx?P=A&amp;S=0&amp;C=0&amp;A=1758&amp;EVetID=900000083" target="_blank">pneumonia</a>, but pneumonia can readily result in any dog or puppy that is sufficiently young, stressed, or debilitated.</p>
<p><strong>Not sure what a Coughing Dog sounds like?</strong></p>
<p>Dogs can make an assortment of respiratory sounds. Usually a cough is recognizable but it is important to be aware of another sound called a reverse sneeze. The reverse sneeze is often mistaken for a cough, a choking fit, sneezing, retching, or even for some sort of respiratory distress. In fact, the reverse sneeze represents a post-nasal drip or tickle in the throat. It is considered normal especially for small dogs or dogs and only requires attention if it is felt to be excessive. The point here is to know a cough when you see one. A cough can be dry or productive, meaning it is followed by a gag, swallowing motion, production of foamy mucus (not to be confused with vomiting). Here are some videos that might help.</p>
