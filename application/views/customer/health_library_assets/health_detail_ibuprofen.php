</p><div class="HideFromPerl" style="float: right;"><img style="margin: 5px; float: right;" src="<?php echo site_url()?>assets/images/library/4129005.jpg" documentid="0" subdocfilerepositoryid="19807803" fileid="4129005" tabletype="20" width="299" height="200"></div>
<p><span style="color: #ff0000;"><strong>Do not give ibuprofen to a <em>dog </em>without specific dosing instructions from your veterinarian.</strong></span></p>
<p><span style="color: #ff0000;"><strong>Do not give ibuprofen to a <em>cat</em> under any circumstances.</strong></span></p>
<p>Ibuprofen has been available as a human pain reliever in the United States since 1974. In 1984, tablets up to 200 mg became over-the-counter and such brands as Motrin, Advil, and Nuprin became household names. Ibuprofen has been prescribed for all manner of human aches and pains as well as for other less common problems. It is a common remedy found in many if not most homes and offices all over America.</p>
<p><strong>Pet Exposure</strong></p>
<p>When pets have pain, people want a simple solution and tend to look in their own medicine cabinet just as they would for themselves. It is unfortunately common for people to make an assumption that a safe medication for people will also be safe for pets and a number of animals are poisoned by ibuprofen when their owner attempts a simple treatment for pain. Alternatively, playful animals may knock over a bottle of pills and swallow an unknown quantity.</p>
<p>Medications are not approved for human over-the-counter use unless they show a good safety margin and their use is difficult to botch. The problem is that every species is different and what is safe for humans can be lethal to a dog or cat.</p>
<p>Never use any medication on your pet without checking with your veterinarian.</p>
<p>Do not attempt to extrapolate dosing from one species to another.</p>
<p><strong>Non-Steroidal Anti-Inflammatory Drugs (NSAIDS)</strong></p>
<p>Non-steroidal anti-inflammatory drugs (called NSAIDs) inhibit an enzyme called cyclooxygenase. This enzyme is involved in the production of inflammatory chemicals called prostaglandins. When the inflammatory cascade is active, cells use their cyclooxygenase enzymes to begin to convert fats from their cell membranes into prostaglandins. NSAIDs put a stop to this.</p>
<p>It turns out that there are several types of cyclooxygenase, however. Some types are involved in producing inflammatory prostaglandins and others involved in producing prostaglandins needed for normal body functions. Ibuprofen is what is called a non-selective cyclooxygenase inhibitor, which means it inhibits all types of cyclooxygenase, not just the ones that produce inflammatory mediators.</p>
<div class="HideFromPerl" style="float: right;"><img style="margin: 5px; width: 333px; height: 325px;" src="<?php echo site_url()?>assets/images/library/4129006.png"  width="333" height="325"></div>
<p>Ibuprofen inhibits prostaglandins involved in the blood supply to the stomach as well as blood supply to the kidneys.</p>
<p>In humans, these effects are minor enough that they did not preclude approval for over-the-counter use but in dogs or cats, these issues are life-threatening. It turns out that dogs and cats are much more sensitive to these issues than people.</p>
<p><em>Stomach Ulceration</em></p>
<p>Ibuprofen has a very narrow safety margin in dogs and, since numerous safer medications are readily available, it is very rarely ever prescribed. Ibuprofen is too toxic for cats at any dose. A typical pill has 200 mg of ibuprofen so only a few pills can be toxic depending on the size of the animal.</p>
<p>The first level of toxicity involves ulceration of the stomach. This leads to vomiting with or without blood, appetite loss, and/or stools that are black from digested blood. The worst case scenario is rupture of the stomach leading to death. Repeated use of ibuprofen will increase the risk of toxicity even at doses that would not be toxic in single exposures.</p>
<p>Ibuprofen inhibits production of prostaglandins needed for normal blood circulation to the stomach. Without normal blood flow, the stomach cannot produce a proper protective layer of mucous to protect its tissues from the harsh digestive acid it contains. Ulceration results. Treatment involves intravenous fluids to restore circulation and medications to heal the ulceration.</p>
<p><em>Kidney Failure</em></p>
<p>The next level of toxicity occurs at higher doses. After interfering with blood flow to the stomach, the blood flow to the kidneys comes next. Reduced blood flow through the kidneys leads to death of kidney tissue. As kidney function decreases, toxins that the kidneys normally remove from the body begin to build up. Damage may be permanent or temporary depending on how much ibuprofen was ingested and how healthy the kidneys were prior to poisoning.</p>
<p>Kidney failure is a metabolic disaster with numerous aspects to be addressed. In the short term, symptoms include: nausea, further ulceration of the gastrointestinal tract, low body temperature, and diarrhea. It may be possible to avoid toxicity of the kidneys by beginning intravenous fluids promptly and supporting circulation despite the NSAIDs in the patient’s system. If toxicity is severe enough to cause the patient to stop making urine, prognosis is substantially worse and treatment must be more aggressive.</p>
<p>Cats are more sensitive to kidney failure effects than are dogs.</p>
<p><em>Neurologic Signs</em></p>
<p>The final level of toxicity is neurologic. At very high doses of ibuprofen, the patient will have tremors that can progress to outright seizures and ultimately coma. The patient will need to be supported with medications to control the involuntary muscle contractions until the ibuprofen is out of the patient’s system.</p>
<p><strong>Treatment and Monitoring</strong></p>
<p>As with other poisoning situations, if the patient is seen promptly (like within an hour or possibly two) it may be possible to induce vomiting. This can be done at your veterinarian’s office or possibly as directed by a toxicologist at the Animal Poison Control Center (APCC, see below). Activated charcoal can be given by your veterinarian to prevent any un-vomited ibuprofen from being absorbed into the body.</p>
<p>Unfortunately, ibuprofen toxicity is common enough that a basic protocol has been put forth by APCC. Typically 48 hours of intravenous fluids are needed to support the stomach and kidneys. Kidney function tests must be monitored and, if possible, this is done at intervals over 3 days following the poisoning event. Medications to prevent stomach ulcers/protect the stomach are frequently needed for a week or so. Strong antacids such as famotidine or omeprazole are commonly used. Sucralfate is often used to form protective webbing over any erosions in the stomach. Misoprostol is a prostaglandin protective to the stomach that can be given orally and is often included in treatment.</p>
<p>Prognosis depends on how much ibuprofen the pet was exposed, for how long, and how complete the treatment is.</p>
